const buildDashboard = (): DashboardSummary => ({
  totalOrders: demoOrders.length,
  processingOrders: demoOrders.filter((order) => ['Đã tiếp nhận', 'Đang phân bổ', 'Đang xử lý', 'Đã đóng gói', 'Đang giao'].includes(order.status)).length,
  deliveredOrders: demoOrders.filter((order) => order.status === 'Giao thành công').length,
  returnOrFailedOrders: demoOrders.filter((order) => ['Giao thất bại', 'Hoàn hàng'].includes(order.status)).length,
  totalInventoryUnits: demoFulfillmentPoints.reduce(
    (sum, point) => sum + point.inventory.reduce((innerSum, item) => innerSum + item.availableQuantity, 0),
    0,
  ),
  channelBreakdown: (['Website', 'Shopee', 'TikTok Shop', 'Facebook', 'Offline'] as SalesChannel[]).map((channel) => ({
    channel,
    totalOrders: demoOrders.filter((order) => order.channel === channel).length,
    revenue: demoOrders.filter((order) => order.channel === channel).reduce((sum, order) => sum + order.totalAmount, 0),
  })),
  recentOrders: [...demoOrders].sort((a, b) => b.createdAt.localeCompare(a.createdAt)).slice(0, 4),
});

